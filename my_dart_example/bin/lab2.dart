void main() {
  // String - text
  String name = 'Soe Tint aung';
  print('Name: $name');

  // int - whole numbers
  int age = 23;
  print('Age: $age');

  // double - decimal numbers
  double height = 5.5;
  print('Height: $height');

  // bool - true or false
  bool isDev = true;
  print('Is Developer: $isDev');

  // var - automatically infers the type
  var city = 'Yangon';
  print('City: $city');

  // final - canâ€™t be changed (runtime constant)
  final country = 'Myanmar';
  print('Country: $country');

  // const - compile-time constant
  const pi = 3.1416;
  print('PI: $pi');
}
